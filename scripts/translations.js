// ── LANG SWITCHER LOGIC ──
(function () {
  const STORAGE_KEY = 'tc-lang';
  let currentLang = localStorage.getItem(STORAGE_KEY) || 'en';

  const LANG_LABELS = { en: 'EN', it: 'IT', zh: '中文' };

  function applyTranslations(lang) {
    const t = TRANSLATIONS[lang];
    if (!t) return;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);

    function setText(id, html) {
      const el = document.getElementById(id);
      if (!el) return;
      if (html && html.includes('<')) el.innerHTML = html;
      else if (el) el.textContent = html || '';
    }

    // sys bar
    setText('t-sys-status', t.sysStatus);
    setText('t-sys-unit', t.sysUnit);
    setText('t-sys-loc', t.sysLoc || t.sysLocArchive);
    setText('t-sys-archive', t.sysArchive || t.sysStatus);

    // nav
    setText('t-nav-photos', t.navPhotos);
    setText('t-nav-stories', t.navStories);
    setText('t-nav-tech', t.navTech);
    setText('t-nav-about', t.navAbout);
    // mobile drawer
    setText('t-nav-photos-m', t.navPhotos);
    setText('t-nav-stories-m', t.navStories);
    setText('t-nav-tech-m', t.navTech);
    setText('t-nav-about-m', t.navAbout);

    // hero
    setText('t-hero-eyebrow', t.heroEyebrow);
    setText('t-hero-code', t.heroCode);
    setText('t-hero-subtitle', t.heroSubtitle);

    // warn strip (all spans share same content pattern)
    ['t-warn-active','t-warn-active-2'].forEach(id => setText(id, t.warnActive));
    ['t-warn-archive','t-warn-archive-2'].forEach(id => setText(id, t.warnArchive));
    ['t-warn-route','t-warn-route-2'].forEach(id => setText(id, t.warnRoute));
    ['t-warn-seat','t-warn-seat-2'].forEach(id => setText(id, t.warnSeat));
    ['t-warn-signal','t-warn-signal-2'].forEach(id => setText(id, t.warnSignal));

    // cards
    setText('t-card1-number', t.card1Number);
    setText('t-card1-title', t.card1Title);
    setText('t-card1-desc', t.card1Desc);
    setText('t-card2-number', t.card2Number);
    setText('t-card2-title', t.card2Title);
    setText('t-card2-desc', t.card2Desc);
    setText('t-card3-number', t.card3Number);
    setText('t-card3-title', t.card3Title);
    setText('t-card3-desc', t.card3Desc);

    // status bar
    setText('t-status-transit', t.statusTransit);
    setText('t-status-pwr', t.statusPwr);
    setText('t-status-conn', t.statusConn);
    setText('t-status-copyright', t.statusCopyright);

    // archive page
    setText('t-archive-eyebrow', t.archiveEyebrow);
    setText('t-archive-title', t.archiveTitle);
    setText('t-archive-sub', t.archiveSub);
    setText('t-archive-loaded', t.archiveLoaded);

    // update button label
    const btn = document.getElementById('lang-btn-label');
    if (btn) btn.textContent = LANG_LABELS[lang];

    // mark active in dropdown
    document.querySelectorAll('.lang-option').forEach(el => {
      el.classList.toggle('lang-option-active', el.dataset.lang === lang);
    });
  }

  function buildSwitcher() {
    const wrapper = document.createElement('div');
    wrapper.className = 'lang-switcher';
    wrapper.innerHTML = `
      <button class="lang-btn" id="lang-btn" aria-label="Change language">
        <span id="lang-btn-label">${LANG_LABELS[currentLang]}</span>
        <span class="lang-btn-arrow">▾</span>
      </button>
      <div class="lang-dropdown" id="lang-dropdown">
        <button class="lang-option" data-lang="en">EN — English</button>
        <button class="lang-option" data-lang="it">IT — Italiano</button>
        <button class="lang-option" data-lang="zh">中文 — 繁體</button>
      </div>
    `;

    document.body.appendChild(wrapper);

    const btn = wrapper.querySelector('#lang-btn');
    const dropdown = wrapper.querySelector('#lang-dropdown');

    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      dropdown.classList.toggle('open');
      btn.classList.toggle('open');
    });

    wrapper.querySelectorAll('.lang-option').forEach(opt => {
      opt.addEventListener('click', () => {
        applyTranslations(opt.dataset.lang);
        dropdown.classList.remove('open');
        btn.classList.remove('open');
      });
    });

    document.addEventListener('click', () => {
      dropdown.classList.remove('open');
      btn.classList.remove('open');
    });
  }

  document.addEventListener('DOMContentLoaded', () => {
    buildSwitcher();
    applyTranslations(currentLang);
  });
})();
